#!/bin/bash


number_1 = 10
number_2 = 5

def addition():
    added = number_1 + number_2
    print(f'Addition of 10 and 5 is {added}')

def subtraction():
    subtract = number_1 - number_2
    print(f'subtraction of 10 and 5 is {subtract}')

def multiplication():
    multiply = number_1 * number_2
    print(f'multiplication of 10 and 5 is {multiply}')

addition()

subtraction()

multiplication()
